// 不存在误差的情况
console.log(0.5 + 0.5); // 1
console.log(0.125 + 0.25); // 0.375
console.log(0.2 + 0.3); // 0.5
// 0.2 => 0.001100110011001100110011001100110011001100110011001101 * 2
// 0.3 => 0.010011001100110011001100110011001100110011001100110110 * 2
// 0.5 => 0.011111111111111111111111111111111111111111111111111111 * 2

// 存在误差的情况
console.log(0.1 + 0.2); // 0.30000000000000004
console.log(0.3 - 0.1); // 0.19999999999999998

// 0.1其实不是0.1
console.log((0.1).toPrecision(21)); // 0.100000000000000005551

// 最大可表示数
console.log(Math.pow(2, 1023)); // 8.98846567431158e+307
console.log(Math.pow(2, 1024)); // Infinity

// 隐式精度转换
const x = 0.1;
console.log(x);
